package arithmetic;

public class Add
{
	public int add(int no1, int no2)
	{
		return (no1 + no2);
	}
	
	public float add(float no1, float no2)
	{
		return (no1 + no2);
	}
	
	public double add(double no1, double no2)
	{
		return (no1 + no2);
	}
	
	public String add(String no1, String no2)
	{
		return (no1 + no2);
	}
}
